class Car:
    def __init__(self, make=None, model=None, year=None, color=None, type=None, mpg=None, available=1, image=None):
        self.make = make
        self.model = model
        self.year = year
        self.color = color
        self.type = type
        self.mpg = mpg
        self.available = available
        self.image = image